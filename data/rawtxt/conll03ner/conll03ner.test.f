Ramthan Hussain
SINGAPORE #-#-#
Singapore 's winning campaign to host the World Trade Organisation ( WTO ) 's first ministerial meeting reflected its ambition to play a key role in shaping global free trade , the lifeblood of its economy , analysts said .
" As one of the world 's most externally oriented economies , Singapore has a disproportionately large stake in the WTO , " said Desmond Supple , economist at research house I.D.E.A .
" Singapore stands to benefit more than most from continued global trade liberalisation as trade is the engine of its growth , accounting for nearly three times its gross domestic product . "
The city-state met U.S. opposition two years ago in its bid to host the meeting , expected to gather #,# officials from # countries from December # to # .
In a stand some analysts linked to controversy over Singapore 's caning of an American teenager for vandalism , then-U.S. Trade Representative Mickey Kantor had said the meeting ought to be held where the WTO was going to be headquartered .
That would have meant Geneva .
But Singapore had the support of other WTO members .
Derek da Cunha , senior fellow at the Institute of Policy Studies ( ISEAS ) , said Singapore 's hosting of the conference " carries a great deal of symbolism for the city-state , underscoring its commitment to free trade and its trading links across the globe . "
There is the international prestige Singapore would enjoy , but " more importantly there is a genuine national interest in fostering better global free trade and an open market " , said Tan Kong Yam , head of Business Policy at the National University of Singapore .
At the ministerial meeting , trade ministers will review the work of the WTO and the implementation of the Uruguay Round free trade commitments under its predecessor the General Agreement on Tariffs and Trade ( GATT ) .
In June , the WTO hailed Singapore for its open market policies but the European Union and other trading powers called on Singapore to speed up the opening of its services sector .
Supple said the struggle that Singapore had to wage in vying to host the meeting would be repeated during the talks .
" There is tension at every step of the way , " since a battle line between the West and developing countries has been drawn over the issue of linking trade liberalisation with labour rights , he said .
Supple said hosting the meeting carried prestige for Singapore , " however , this is quite intangible as the prestige factor may not necessarily lead to any additional investment and trade flows to this region . "
From a commercial point of view , the meeting would be good for Singapore 's tourism industry , Tan said .
A large part of Singapore 's workforce would be mobilised to ensure the meeting would run without a glitch but the average Singaporean " would probably not be too concerned about some of the issues , " Tan said .
" But the more educated public will realise that these kind of things are important for Singapore as a small economy . "
Supple said any political gains the Singapore government would get from the WTO meeting -- ahead of a general election due by April # -- would depend on how successful it was in pushing its economic agenda .
" If there are any movements toward freer trade , then Singapore 's economy and the electorate would gain , " he said . "
But I do n't think it would be wise to play up the political aspect of this .
